#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	print_nb(int nb)
{
	if (nb >= 10)
		print_nb(nb / 10);
	ft_putchar((nb % 10) + '0');
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			ft_putchar('-');
			nb *= -1;
		}
		print_nb(nb);
	}
}
