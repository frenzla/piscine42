#include <unistd.h>

void	ft_print_comb(void)
{
	char	u;
	char	d;
	char	c;

	c = '0';
	while (c < '8')
	{
		d = c + 1;
		while (d < '9')
		{
			u = d + 1;
			while (u <= '9')
			{
				write(1, &c, 1);
				write(1, &d, 1);
				write(1, &u, 1);
				if (c != '7')
					write(1, ", ", 2);
				u++;
			}
		d++;
		}
	c++;
	}
}
